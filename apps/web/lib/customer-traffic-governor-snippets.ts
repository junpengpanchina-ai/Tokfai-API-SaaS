const CHAT_MODEL = "auto-fast";
const IMAGE_MODEL = "gpt-image-2";
const CHAT_PROMPT = "Say ok only.";
const IMAGE_PROMPT = "A simple product photo on white background.";

const RETRYABLE_JS = RETRYABLE_ERROR_CODES.map((c) => `"${c}"`).join(", ");
const NON_RETRYABLE_JS = NON_RETRYABLE_ERROR_CODES.map((c) => `"${c}"`).join(", ");
