/** Node.js traffic governor — queue + maxConcurrent + safe retry (Chat) */
export function buildNodeTrafficGovernor(apiKey = TOKFAI_API_KEY_PLACEHOLDER): string {
  const lines = [
    "// Save as tokfai-worker.mjs — run: node tokfai-worker.mjs (any folder)",
    `const API_KEY = process.env.TOKFAI_API_KEY ?? "${apiKey}";`,
    `const CHAT_URL = "${TOKFAI_API_BASE_URL}/chat/completions";`,
    `const MAX_CONCURRENT = ${DEFAULT_CHAT_MAX_CONCURRENT};`,
    `const MAX_ATTEMPTS = ${DEFAULT_MAX_ATTEMPTS};`,
    "const DELAYS_MS = [5000, 15000, 30000];",
    `const RETRYABLE = new Set([${RETRYABLE_JS}]);`,
    `const NON_RETRYABLE = new Set([${NON_RETRYABLE_JS}]);`,
    "",
    "function sleep(ms) {",
    "  return new Promise((r) => setTimeout(r, ms));",
    "}",
    "",
    "function isRetryable(status, body) {",
    "  if (status === 401 || status === 402 || status === 400 || status === 413) return false;",
    "  const code = body?.error?.code;",
    "  if (code && NON_RETRYABLE.has(code)) return false;",
    "  if (status === 429 || status === 503 || status === 504) return true;",
    "  if (status >= 500 && status < 600) return code ? RETRYABLE.has(code) : true;",
    "  return false;",
    "}",
    "",
    "async function callChat(content) {",
    "  const payload = {",
    `    model: "${CHAT_MODEL}",`,
    "    messages: [{ role: \"user\", content }],",
    "    stream: false,",
    "  };",
    "  for (let attempt = 0; attempt < MAX_ATTEMPTS; attempt++) {",
    "    const res = await fetch(CHAT_URL, {",
    "      method: \"POST\",",
    "      headers: {",
    "        Authorization: `Bearer ${API_KEY}`,",
    "        \"Content-Type\": \"application/json\",",
    "      },",
    "      body: JSON.stringify(payload),",
    "    });",
    "    const body = await res.json();",
    "    if (res.ok) {",
    "      const tokfai = body.tokfai ?? {};",
    "      console.log(\"request_id:\", body.request_id ?? tokfai.request_id);",
    "      console.log(\"credits_charged:\", body.credits_charged ?? tokfai.credits_charged);",
    "      console.log(\"resolved_model:\", tokfai.resolved_model ?? body.model);",
    "      return body;",
    "    }",
    "    if (!isRetryable(res.status, body) || attempt === MAX_ATTEMPTS - 1) {",
    "      throw new Error(`${body.error?.code ?? res.status}: ${body.error?.message ?? \"failed\"}`);",
    "    }",
    "    const delay = DELAYS_MS[Math.min(attempt, DELAYS_MS.length - 1)] + Math.random() * 3000;",
    "    console.warn(`retry ${body.error?.code} — wait ${Math.round(delay)}ms`);",
    "    await sleep(delay);",
    "  }",
    "}",
    "",
    "class TrafficGovernor {",
    "  constructor(maxConcurrent) {",
    "    this.max = maxConcurrent;",
    "    this.active = 0;",
    "    this.queue = [];",
    "  }",
    "  run(task) {",
    "    return new Promise((resolve, reject) => {",
    "      this.queue.push({ task, resolve, reject });",
    "      this.pump();",
    "    });",
    "  }",
    "  pump() {",
    "    while (this.active < this.max && this.queue.length) {",
    "      const { task, resolve, reject } = this.queue.shift();",
    "      this.active++;",
    "      task()",
    "        .then(resolve)",
    "        .catch(reject)",
    "        .finally(() => {",
    "          this.active--;",
    "          this.pump();",
    "        });",
    "    }",
    "  }",
    "}",
    "",
    "const governor = new TrafficGovernor(MAX_CONCURRENT);",
    "const prompts = [\"Say ok.\", \"Say hi.\", \"Say hey.\"];",
    "await Promise.all(",
    "  prompts.map((content) => governor.run(() => callChat(content)))",
    ");",
    "console.log(\"done\", prompts.length, \"jobs\");",
  ];
  return lines.join("\n");
}

